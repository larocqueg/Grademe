#include <stddef.h>

size_t	ft_strcspn(const char *s, const char *reject)
{
	size_t size = 0;
	int	i = 0;
	int	j;
	while (s[i])
	{
		j = 0;
		while (reject[j])
		{
			if (s[i] == reject[j])
				return (i);
			j++;
		}
		i++;
	}
	return (i);
}
